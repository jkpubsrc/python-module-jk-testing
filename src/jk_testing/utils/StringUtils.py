




#
# Various utility methods used to assist in string formatting.
#
class StringUtils(object):

	################################################################################################################################
	## Constants
	################################################################################################################################

	################################################################################################################################
	## Constructor
	################################################################################################################################

	################################################################################################################################
	## Public Properties
	################################################################################################################################

	################################################################################################################################
	## Helper Methods
	################################################################################################################################

	################################################################################################################################
	## Public Methods
	################################################################################################################################

	################################################################################################################################
	## Static Methods
	################################################################################################################################

	@staticmethod
	def padRight(s:str, maxLen:int, c:str = " ", slen:int = None) -> str:
		if slen is None:
			slen = len(s)
		n = maxLen - slen
		return s + n * c
	#

	@staticmethod
	def padLeft(s:str, maxLen:int, c:str = " ", slen:int = None) -> str:
		if slen is None:
			slen = len(s)
		n = maxLen - slen
		return n * c + s
	#

